  document.getElementById('langLbl').textContent = l.toUpperCase();
  document.querySelectorAll('[data-i]').forEach(function(el) {
    var v = T[l][el.getAttribute('data-i')];
    if (v !== undefined) el.innerHTML = v;
  });

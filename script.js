// ── FORM: validação no cliente ──
function validateForm() {
  var ok = true;

  var name = document.getElementById('f-name');
  var errName = document.getElementById('err-name');
  if (!name.value.trim() || name.value.trim().length < 2) {
    errName.textContent = T[lang].f_err_name;
    name.classList.add('invalid'); name.classList.remove('valid');
    ok = false;
  } else {
    errName.textContent = '';
    name.classList.remove('invalid'); name.classList.add('valid');
  }

  var email = document.getElementById('f-email');
  var errEmail = document.getElementById('err-email');
  var emailRe = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
  if (!email.value.trim() || !emailRe.test(email.value.trim())) {
    errEmail.textContent = T[lang].f_err_email;
    email.classList.add('invalid'); email.classList.remove('valid');
    ok = false;
  } else {
    errEmail.textContent = '';
    email.classList.remove('invalid'); email.classList.add('valid');
  }

  var msg = document.getElementById('f-msg');
  var errMsg = document.getElementById('err-msg');
  if (!msg.value.trim() || msg.value.trim().length < 10) {
    errMsg.textContent = T[lang].f_err_msg_short;
    msg.classList.add('invalid'); msg.classList.remove('valid');
    ok = false;
  } else if (msg.value.length > 1000) {
    errMsg.textContent = T[lang].f_err_msg_long;
    msg.classList.add('invalid'); msg.classList.remove('valid');
    ok = false;
  } else {
    errMsg.textContent = '';
    msg.classList.remove('invalid'); msg.classList.add('valid');
  }

  return ok;
}

// ── FORM: submit via fetch → backend ──
document.getElementById('cForm').addEventListener('submit', function(e) {
  e.preventDefault();

  var status = document.getElementById('f-status');
  var btn = document.getElementById('submitBtn');

  // Honeypot check no cliente também
  if (document.getElementById('hp').value) return;

  if (!validateForm()) return;

  var payload = {
    name   : document.getElementById('f-name').value.trim(),
    company: document.getElementById('f-company').value.trim(),
    email  : document.getElementById('f-email').value.trim(),
    message: document.getElementById('f-msg').value.trim(),
    website: document.getElementById('hp').value, // honeypot
  };

  btn.disabled = true;
  btn.textContent = T[lang].f_sending;
  status.textContent = T[lang].f_sending;
  status.className = 'f-status sending';
  status.style.display = '';

  // Envia direto para o Formspree
  fetch('https://formspree.io/f/mdawddwj', {
    method : 'POST',
    headers: { 'Content-Type': 'application/json', 'Accept': 'application/json' },
    body   : JSON.stringify(payload),
  })
  .then(function(res) { return res.json().then(function(d) { return { ok: res.ok, data: d }; }); })
  .then(function(r) {
    if (r.ok) {
      status.textContent = T[lang].f_success;
      status.className = 'f-status success';
      e.target.reset();
      if (counter) { counter.textContent = '0 / 1000'; counter.className = 'f-counter'; }
      document.querySelectorAll('.f-in,.f-ta').forEach(function(el) { el.classList.remove('valid','invalid'); });
    } else {
      status.textContent = r.data.error || T[lang].f_error;
      status.className = 'f-status error';
    }
    btn.disabled = false;
    btn.textContent = T[lang].f_send;
  })
  .catch(function() {
    status.textContent = T[lang].f_conn;
    status.className = 'f-status error';
    btn.disabled = false;
    btn.textContent = T[lang].f_send;
  });
});


(function() {
  // Defer starfield until browser is idle — avoids blocking LCP
  function initStarfield() {
  var canvas = document.getElementById('starfield');
  if (!canvas) return;
  var ctx = canvas.getContext('2d');
  var W, H, stars = [], nebulas = [], shoots = [];
  function resize() { W = canvas.width = window.innerWidth; H = canvas.height = window.innerHeight; }
  resize();
  window.addEventListener('resize', resize);
  for (var i = 0; i < 200; i++) {
    stars.push({ x:Math.random(), y:Math.random(), r:Math.random()*1.4+0.2, o:Math.random()*0.6+0.2, sp:Math.random()*0.02+0.005, ph:Math.random()*6.28 });
  }
  for (var j = 0; j < 4; j++) {
    nebulas.push({ x:Math.random(), y:Math.random(), r:Math.random()*280+100, h:200+Math.random()*40, o:Math.random()*0.05+0.02 });
  }
  setInterval(function() {
    if (Math.random() > 0.35) return;
    shoots.push({ x:Math.random()*W, y:Math.random()*H*0.4, vx:6+Math.random()*5, vy:3+Math.random()*3, a:1 });
  }, 3000);
  var t = 0;
  var last = 0;
  function draw(ts) {
    requestAnimationFrame(draw);
    if (ts - last < 33) return;
    last = ts;
    ctx.clearRect(0, 0, W, H);
    nebulas.forEach(function(n) {
      var g = ctx.createRadialGradient(n.x*W,n.y*H,0,n.x*W,n.y*H,n.r);
      g.addColorStop(0,'hsla('+n.h+',70%,50%,'+n.o+')'); g.addColorStop(1,'transparent');
      ctx.fillStyle=g; ctx.beginPath(); ctx.arc(n.x*W,n.y*H,n.r,0,6.28); ctx.fill();
    });
    stars.forEach(function(s) {
      var fl = s.o*(0.75+0.25*Math.sin(t*s.sp+s.ph));
      ctx.beginPath(); ctx.arc(s.x*W,s.y*H,s.r,0,6.28);
      ctx.fillStyle='rgba(255,255,255,'+fl+')'; ctx.fill();
      if (s.r>1.1) { ctx.beginPath(); ctx.arc(s.x*W,s.y*H,s.r*3,0,6.28); ctx.fillStyle='rgba(80,140,255,'+(fl*0.07)+')'; ctx.fill(); }
    });
    shoots = shoots.filter(function(s){return s.a>0;});
    shoots.forEach(function(s) {
      var g = ctx.createLinearGradient(s.x,s.y,s.x-s.vx*10,s.y-s.vy*10);
      g.addColorStop(0,'rgba(255,255,255,'+s.a+')'); g.addColorStop(1,'transparent');
      ctx.strokeStyle=g; ctx.lineWidth=1.2;
      ctx.beginPath(); ctx.moveTo(s.x,s.y); ctx.lineTo(s.x-s.vx*10,s.y-s.vy*10); ctx.stroke();
      s.x+=s.vx; s.y+=s.vy; s.a-=0.02;
    });
    t++;
  }
  requestAnimationFrame(draw);
  } // end initStarfield
  if ('requestIdleCallback' in window) {
    requestIdleCallback(initStarfield, { timeout: 2000 });
  } else {
    setTimeout(initStarfield, 200);
  }
})();


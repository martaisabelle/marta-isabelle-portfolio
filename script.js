function updateClock() {
  var t = T[lang];
  var now = new Date();
  var utc = now.getTime() + (now.getTimezoneOffset() * 60000);
  var brt = new Date(utc - (3 * 3600000));
  var h = brt.getHours(), m = brt.getMinutes();
  var min = h * 60 + m;
  var hh = (h < 10 ? '0' : '') + h;
  var mm = (m < 10 ? '0' : '') + m;
  document.getElementById('clockT').textContent = hh + ':' + mm;
  document.getElementById('clockP').textContent = min >= 360 && min < 720 ? t.pm : (min >= 720 && min < 1080 ? t.pa : t.pe);
}
updateClock();
